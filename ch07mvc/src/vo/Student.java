package vo;

public class Student {
	// For student
	private String sid;
	private String sname;
	private int iage;

	public String getSid() {
		return this.sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getSname() {
		return this.sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public int getIage() {
		return this.iage;
	}

	public void setIage(int iage) {
		this.iage = iage;
	}
}
